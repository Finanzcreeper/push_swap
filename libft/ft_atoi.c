int	ft_atoi(const char *nptr)
{
	int	c;
	int	o;
	int	rev;

	o = 0;
	c = 0;
	rev = 1;
	while ((nptr[c] >= 9 && nptr[c] <= 13) || nptr[c] == ' ')
	{
		c++;
	}
	if (nptr[c] == '+' || nptr[c] == '-')
	{
		if (nptr[c] == '-')
			rev = -1;
		c++;
	}
	while (nptr[c] >= '0' && nptr[c] <= '9')
	{
		o = (nptr[c] - 48) + (o * 10);
		c++;
	}
	return (o * rev);
}

#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	char	o;

	if (fd < 0)
		return ;
	if (n == -2147483648)
	{
		write(fd, "-2147483648", 11);
		return ;
	}
	if (n < 0)
	{
		n = n * -1;
		write(fd, "-", 1);
	}
	if (n > 9)
		ft_putnbr_fd(n / 10, fd);
	o = (n % 10) + '0';
	write(fd, &o, 1);
}
